package com.model;

public class FoodItems {
	private String name;
	private double price;

	public FoodItems(String name, double price) {
		super();
		this.name = name;
		this.price = price;
	}

	public String getName() {
		return name;
	};
	
	public double getPrice() {
		return price;
	}
	

}
